package _java.introduction;

public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        a += b;
        System.out.println(a);
        a -= b;
        System.out.println(a);
        a *= b;
        System.out.println(a);
        a /= b;
        System.out.println(a);
        a %= b;
        System.out.println(a);
        a &= b;
        System.out.println(a);
        a |= b;
        System.out.println(a);
        a ^= b;
        System.out.println(a);
        a <<= b;
        System.out.println(a);
        a >>= b;
        System.out.println(a);
    }
}
